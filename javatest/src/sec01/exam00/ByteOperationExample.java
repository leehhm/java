package sec01.exam00;

public class ByteOperationExample {

	public static void main(String[] args) {
		byte result1 = 10 + 20;
		System.out.println(result1);
		
		byte x = 10;
		byte y = 20;
		int result2 = x + y;
		// 특별한 경우 아니라면 정수 연산에 사용하는 변수는 int 타입으로 선언하는 것이 효과적
		System.out.println(result2);

	}

}