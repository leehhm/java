package testexample;

public class Printer {
	
	static void printer(int value) {
		System.out.println(value);
	}
	
	static void printer(boolean value) {
		System.out.println(value);
	}
	
	static void printer(double value) {
		System.out.println(value);
	}
	
	static void printer(String value) {
		System.out.println(value);
	}
}
